#import "SDCollectionViewCell.h"
#import "UIView+SDExtension.h"

@implementation SDCollectionViewCell
{
    __weak UILabel *_titleLabel;
}


- (instancetype)initWithFrame:(CGRect)frame
{
    if (self = [super initWithFrame:frame]) {
        [self setupImageView];
        [self setupTitleLabel];
        // æ–°å¢è’™ç‰ˆView
        [self setupMaskView];
    }
    
    return self;
}

- (void)setupMaskView
{
    SDCustomMaskView *customMask = [[SDCustomMaskView alloc] init];
    customMask.backgroundColor = [[UIColor blackColor] colorWithAlphaComponent:0.3];
    _mkjBannerMask = customMask;
    [self.contentView addSubview:customMask];
}

- (void)setTitleLabelBackgroundColor:(UIColor *)titleLabelBackgroundColor
{
    _titleLabelBackgroundColor = titleLabelBackgroundColor;
    _titleLabel.backgroundColor = titleLabelBackgroundColor;
}

- (void)setTitleLabelTextColor:(UIColor *)titleLabelTextColor
{
    _titleLabelTextColor = titleLabelTextColor;
    _titleLabel.textColor = titleLabelTextColor;
}

- (void)setTitleLabelTextFont:(UIFont *)titleLabelTextFont
{
    _titleLabelTextFont = titleLabelTextFont;
    _titleLabel.font = titleLabelTextFont;
}

- (void)setupImageView
{
    UIImageView *imageView = [[UIImageView alloc] init];
    _imageView = imageView;
    [self.contentView addSubview:imageView];
}

- (void)setupTitleLabel
{
    UILabel *titleLabel = [[UILabel alloc] init];
    _titleLabel = titleLabel;
    _titleLabel.hidden = YES;
    [self.contentView addSubview:titleLabel];
}

- (void)setTitle:(NSString *)title
{
    _title = [title copy];
    _titleLabel.text = [NSString stringWithFormat:@"   %@", title];
}


- (void)layoutSubviews
{
    [super layoutSubviews];
    
    _imageView.frame = self.bounds;
    // æœ€åä¸€æ­¥ï¼Œå½“å¸ƒå±€cellå­è§†å›¾çš„æ—¶å€™è°ƒç”¨ï¼Œè¿™ä¸ªæ—¶å€™éœ€è¦æ ¹æ®å¤–éƒ¨æ¥å£YES or NOæ¥è¿›è¡Œæ˜¯å¦ç»™Frame
    // é»˜è®¤å­—æ®µæ˜¯NOï¼Œå½“YESçš„æ—¶å€™å°±åŠ è½½ï¼Œè¿™é‡Œå¯ä»¥æ ¹æ®å…·ä½“çš„éœ€æ±‚éšæ„æ›´æ”¹ï¼Œå„ä½æƒ³æ€ä¹ˆåŠ è½½å°±åŠ è½½ä»€ä¹ˆæ ·å¼
    if (self.isShow) {
        _mkjBannerMask.frame = self.bounds;
    }
    CGFloat titleLabelW = self.sd_width;
    CGFloat titleLabelH = _titleLabelHeight;
    CGFloat titleLabelX = 0;
    CGFloat titleLabelY = self.sd_height - titleLabelH;
    _titleLabel.frame = CGRectMake(titleLabelX, titleLabelY, titleLabelW, titleLabelH);
    _titleLabel.hidden = !_titleLabel.text;
}

@end
